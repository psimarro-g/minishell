#include"../inc/libft.h"

void	ft_strrev(char *s)
{
	size_t	i;

	i = 0;
	while (i < ft_strlen(s) / 2)
	{
		ft_swap(&s[i], &s[ft_strlen(s) - i - 1]);
		i++;
	}
}
